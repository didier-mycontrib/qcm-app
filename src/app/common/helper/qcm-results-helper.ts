import { FieldHelper } from "../../shared/helper/field-helper";
import { ObjectHelper } from "../../shared/helper/object-helper";
import {QcmResults } from "../data/qcm";

export class QcmResultsHelper implements ObjectHelper<QcmResults,String> {
        withAutoGeneratedId(): boolean { return false;  }
        getId(obj: QcmResults): string { return obj.id??"?"; };
        getIdKeyName(): string {return "id"; }
        buildEmptyObject(): QcmResults { return new QcmResults(); };
        getEntityTypeName(): string { return "QcmResults";  };
        essentialFieldNames(): string[] {return ["id"]; }
        getFieldHelper(fieldName:string):FieldHelper|null{
                switch(fieldName){
                        case "id" : return new FieldHelper("id");
                        default : return null;//NB:FieldHelper(fieldName,fieldType,defaultValue)
                }
        }
}