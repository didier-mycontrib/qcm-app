import { FieldHelper } from "../../shared/helper/field-helper";
import { AbstractObjectHelper } from "../../shared/helper/object-helper";
import {QcmResults } from "../data/qcm";

export class QcmResultsHelper  extends AbstractObjectHelper<QcmResults,String> {
        constructor(){
                super("QcmResults",true,"id"); //EntityName,withAutoGeneratedId,idKeyName
        }
        getId(obj: QcmResults): string { return obj.id??"?"; };
        buildEmptyObject(): QcmResults { return new QcmResults(); };
        initObjectHelper(){
               this.classHelper.essentialFieldNames = ["id"]
               this.classHelper.fieldHelperMap.set("id",new FieldHelper("id"))
               //NB:FieldHelper(fieldName,fieldType,defaultValue)
        }
}